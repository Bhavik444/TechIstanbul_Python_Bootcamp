#karşılaştırma operatörleri
#genel olarak sayılarda kullanılır
#stringler vb yapılarda eşitliği kontrol amacıyla kullanılır
# işlem sonuçları her zaman mantıksal olur yani True - False
# eşitlik kontrolü için ==
print(9 == 8)
print(5 == 5)
print("ali" == "ayşe")
print("bora" == "bora")
#eşitsizlik kontrolü için !=
print(9 != 9)
print("ali" != "ayşe")
print(True == True)
print("8" == 8)

#küçüktür < , küçüktür eşittir <=
#büyüktür > , büyüktür eşittir >=
print(5 < 6)
print( 5 <= 6)

print(5 >= 8)
print(5 > 8)
#örnek uygulama
print("ehliyet alma durumunuz", 18 <= 18)   
print("ehliyet alma durumunuz", 12 >= 18)